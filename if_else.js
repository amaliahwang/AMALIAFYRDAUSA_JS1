// if,else,nested if

let Kecepatan = 75;

if (Kecepatan >= 90) {
  console.log("Kecepatan Anda Melebih Batas Maksimal Kecepatan");
} else if (Kecepatan >= 80) {
  console.log("Kecepatan Anda Berada Tepat Di Batas Kecepatan");
} else if (Kecepatan >= 70) {
  console.log("Kecepatan Anda Berada Di Batas Aman Kecepatan");
} else {
  console.log("Kecepatan Anda Berada Di Bawah Batas Kecepatan");
}